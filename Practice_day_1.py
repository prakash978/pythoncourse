# Type casting ( converstion of int=>float=>string )

# int==>float

x = 20 
y = float(x)
print(type(y),y)


# float==>int

x = 50.5

y = int(x)
print(type(y),y)



# string==>int

x = "500"
y = int(x)
print(type(y),y)


# string==>float

x = "600"
y = float(x)
print(type(y),y)


#int==>string

x = 100
y = str(x)
print(type(y),y)




